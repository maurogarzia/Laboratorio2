public class Punto {
    private int x;
    private int y;

    //Constructor
    public Punto(int x, int y) {
        this.x = x;
        this.y = y;
    }
    //Getter
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    //Setter
    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
